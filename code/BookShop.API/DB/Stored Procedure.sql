CREATE PROCEDURE sp_GetBooks(
	In p_UserId INT
)
Begin
	Select 
		BOSD03.D03F01,
		BOSD03.D03F02,
		BOSD03.D03F03,
		BOSD03.D03F04,
		BOSD03.D03F05,
        	BOSD03.D03F06,
		BOSD03.D03F07,
        	BOSD01.D01F02
	From
		BOSD03
	Join
		BOSD01
	on
		BOSD01.D01F01 = BOSD03.D03F06
	Where 
		BOSD03.D03F07 = p_UserId;
End //
DELIMITER;